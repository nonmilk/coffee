package io.github.nonmilk.coffee;

public final class Selection {

    private int x;
    private int y;

    private int width;
    private int height;

    public Selection() {
    }

    public int x() {
        return x;
    }

    public int y() {
        return y;
    }

    public int width() {
        return width;
    }

    public int height() {
        return height;
    }
}
